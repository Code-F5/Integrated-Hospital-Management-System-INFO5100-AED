    private void populateTable() {
         //To change body of generated methods, choose Tools | Templates.
        DonorDirectory donorList = ecosystem.getDonorDirectory();
        DefaultTableModel model = (DefaultTableModel) tblDonor.getModel();
        model.setRowCount(0);
        for (Donor donor: donorList.getDonorList()){
            Object[] row = new Object[4];
            row[0] = donor;
            row[1] = donor.getAddress();
            row[2] = donor.getEmail();
            row[3] = donor.getPhone();
            row[4] = donor.getAccountNo();
            
            
        }        
    }
