    private void txtPhoneKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhoneKeyReleased
        // TODO add your handling code here:
        Pattern pattern = Pattern.compile("^\\d{10}$");
        Matcher match = pattern.matcher(txtPhone.getText());
        if(!match.matches()){
            lblPhoneValidation.setText("Incorrect!");
        }
        else lblPhoneValidation.setText(null);
        
    }//GEN-LAST:event_txtPhoneKeyReleased

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().trim().isEmpty() || txtAddress.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty() ||
                txtPhone.getText().trim().isEmpty() || txtUsername.getText().trim().isEmpty() || txtPassword.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all the mandatory fields.");
            return;
            
        } else 
            if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtUsername.getText())){
                Donor donor = new Donor();
                donor.setAddress(txtAddress.getText().trim());
                donor.setName(txtName.getText().trim());
                donor.setPhone(txtPhone.getText());
                donor.setEmail(txtEmail.getText());
                donor.setUsername(txtUsername.getText());
                donor.setPassword(txtPassword.getText());
                donor.setAccountNo(txtAccountNo.getText());
                donor.setRole(new DonorRole());
                ecosystem.getUserAccountDirectory().addUserAccount(donor);
                ecosystem.getDonorDirectory().addDonor(donor);
                populateTable();
                
                txtName.setText("");
                txtAddress.setText("");
                txtPhone.setText("");
                txtEmail.setText("");
                txtUsername.setText("");
                txtPassword.setText("");
                txtAccountNo.setText("");
                
            }else{
            JOptionPane.showMessageDialog(null, "Username " + txtUsername.getText() + " already exists !!!, Please try a new one");
         }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDonor.getSelectedRow();
         if (selectedRow >= 0){
             Donor donor = (Donor) tblDonor.getValueAt(selectedRow, 0);
             donor.setName(txtName.getText());
             donor.setAccountNo(txtAccountNo.getText());
             donor.setEmail(txtEmail.getText());
             donor.setPhone(txtPhone.getText());
             donor.setAddress(txtAddress.getText());
             JOptionPane.showMessageDialog(null, "Customer   "  + txtUsername.getText() + " updated");
             populateTable();
             
             txtName.setText("");
             txtAccountNo.setText("");
             txtEmail.setText("");
             txtPhone.setText("");
             txtAddress.setText("");
             txtUsername.setText("");
             txtPassword.setText("");
             
         }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDonor.getSelectedRow();
        if(selectedRow >=0 ){
            Donor donor = (Donor)(tblDonor.getValueAt(selectedRow, 0));
            DonorDirectory donorDirectory = ecosystem.getDonorDirectory();
            donorDirectory.removeDonor(donor);
            JOptionPane.showMessageDialog(null, "Donor   "  + txtUsername.getText() + " deleted");
            populateTable();
            txtName.setText("");
            txtAccountNo.setText("");
            txtEmail.setText("");
            txtPhone.setText("");
            txtAddress.setText("");
            txtUsername.setText("");
            txtPassword.setText("");
                             
        } else 
             JOptionPane.showMessageDialog(null, "Please select a row.");
    }//GEN-LAST:event_btnDeleteActionPerformed


