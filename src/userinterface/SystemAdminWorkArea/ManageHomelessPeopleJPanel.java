    private void populateTable() {
        HomelessPeopleDirectory homelessPeopleList = ecosystem.getHomelessPeopleDirectory();
        DefaultTableModel model = (DefaultTableModel) tblHomeless.getModel();
        
        model.setRowCount(0);
        for (HomelessPerson hp: homelessPeopleList.getHomelessDirectory()){
            Object[] row = new Object[5];
            row[0] = hp;
            row[1] = hp.getDateOfBirth();
            row[2] = hp.getGender();
            row[3] = hp.getBloodType();
            row[4] = hp.getPhone();
            model.addRow(row);
                
        }
    }
