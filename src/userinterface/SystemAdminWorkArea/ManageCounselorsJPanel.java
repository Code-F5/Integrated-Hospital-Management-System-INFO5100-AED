    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().trim().isEmpty() || txtSSN.getText().trim().isEmpty() || txtPhone.getText().trim().isEmpty() || txtPhone.getText().trim().isEmpty()
                || (!rbMale.isSelected() && !rbFemale.isSelected()) || dcDOB.getDate().toString().trim().isEmpty() || txtUsername.getText().trim().isEmpty()
                || txtPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all mandatory fields.");
            return;
            
        }
        if (ecosystem.checkIfUserIsUnique(txtUsername.getText())){
            Counselor counselor = new Counselor();
        counselor.setName(txtName.getText());
        
        if(rbMale.isSelected())
            counselor.setGender("Male");
        else
            counselor.setGender("Female");
        counselor.setSsn(txtSSN.getText().trim());
        counselor.setPhone(txtPhone.getText().trim());
        counselor.setDateOfBirth(dcDOB.getDate().toString());
        counselor.setUsername(txtUsername.getText());
        counselor.setPassword(txtPassword.getText());
        counselor.setRole(new CounselorRole());
        ecosystem.getUserAccountDirectory().addUserAccount(counselor);
        ecosystem.getCounselorDirectory().addCounselor(counselor);
        populateTable();
        
        
        txtName.setText("");
        txtPhone.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        
        }
        else JOptionPane.showMessageDialog(null, "Username " + txtUsername.getText() + " already exists, Please try another one");
       
        
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow  = tblCounselor.getSelectedRow();
        if(selectedRow >=0){
            Counselor counselor = (Counselor) tblCounselor.getValueAt(selectedRow, 0);
            
            counselor.setName(txtName.getText());
        
        if(rbMale.isSelected())
            counselor.setGender("Male");
        else
            counselor.setGender("Female");
        counselor.setSsn(txtSSN.getText().trim());
        counselor.setPhone(txtPhone.getText().trim());
        counselor.setDateOfBirth(dcDOB.getDate().toString());
        counselor.setUsername(txtUsername.getText());
        counselor.setPassword(txtPassword.getText());
        JOptionPane.showMessageDialog(null, "Counselor   "  + txtUsername.getText() + " updated");
        populateTable();
        txtName.setText("");
        txtPhone.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        
            
        } else JOptionPane.showMessageDialog(null, "Please select a row.");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow  = tblCounselor.getSelectedRow();
        if(selectedRow >=0){
            Counselor counselor = (Counselor) tblCounselor.getValueAt(selectedRow, 0);
            CounselorDirectory counselorList = ecosystem.getCounselorDirectory();
            counselorList.removeCounselor(counselor);
            JOptionPane.showMessageDialog(null, "Donor   "  + txtUsername.getText() + " deleted");
            populateTable();
            txtName.setText("");
            txtPhone.setText("");
            txtUsername.setText("");
            txtPassword.setText("");
            
        } else JOptionPane.showMessageDialog(null, "Please select a row.");
    }//GEN-LAST:event_btnDeleteActionPerformed


