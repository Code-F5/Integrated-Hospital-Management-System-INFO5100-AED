    private void populateTable() {
        CounselorDirectory counselorList = ecosystem.getCounselorDirectory();
        DefaultTableModel model = (DefaultTableModel) tblCounselor.getModel();
        model.setRowCount(0);
        for (Counselor counselor: counselorList.getCounselorDirectory()){
            Object[] row = new Object[5];
            row[0] = counselor;
            row[1] = counselor.getType();
            row[2] = counselor.getSsn();
            row[3] = counselor.getPhone();
            row[4] = counselor.getGender();
            row[5] = counselor.getDateOfBirth();
            
            model.addRow(row);
            
        }
    }
