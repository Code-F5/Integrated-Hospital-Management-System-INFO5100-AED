    private boolean passwordPatternValidation() {
        Pattern p = Pattern.compile( "^(?=.*[0-9])"
                       + "(?=.*[a-z])(?=.*[A-Z])"
                       + "(?=.*[@#$%^&+=])"
                       + "(?=\\S+$).{5,20}$");
        Matcher m = p.matcher(String.valueOf(txtPassword.getPassword()));
        return m.matches();
    }

